#include "libft.h"

void		*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*a;
	char	*b;

	a = (char *)dst;
	b = (char *)src;
	while (n--)
		*a++ = *b++;
	return (dst);
}
